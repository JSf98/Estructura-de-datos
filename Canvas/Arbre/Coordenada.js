class Coordenada{

  constructor(x, y){
    this.x = x
    this.y = y
  }

  getPosX(){
    return this.x
  }

  getPosY(){
    return this.y
  }

  setPosX(x){
    this.x = x
  }

  setPosY(y){
    this.y = y
  }
}

